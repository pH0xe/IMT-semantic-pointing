const random = (min, max) => Math.random() * (max - min) + min;

export const generateCrosses = (count) => {
  const crosses = [];
  for (let i = 0; i < count; i++) {
    crosses.push(
      generateCross(
        random(0.1, 1),
        random(-5, 5),
        random(-5, 5),
        random(-5, 5),
        random(0, 360),
        random(0, 360),
        random(0, 360)
      )
    );
  }
  return crosses;
};

